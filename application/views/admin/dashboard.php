    function tambahSaldoCash() {
        var sal_in = document.getElementById("sal_inCash").value;

        // Validasi input saldo cash
        if (sal_in.trim() === '') {
            showSweetAlert('error', 'Jumlah saldo harus diisi.');
            return;
        }

        var xhttp = new XMLHttpRequest();

        xhttp.onreadystatechange = function() {
            if (this.readyState == 4 && this.status == 200) {
                // console.log(this.responseText);
                var response = JSON.parse(this.responseText);
                showSweetAlert(response.status, response.message);
                if (response.status === 'success') {
                    var saldoCashModal = document.getElementById("saldoCashModal");
                    var modalInstance = bootstrap.Modal.getInstance(saldoCashModal);
                    modalInstance.hide();
                    setTimeout(() => {
                        window.location.reload();
                    }, 1500);
                }
            }
        };

        var params = "sal_inCash=" + encodeURIComponent(sal_in);

        xhttp.open("POST", "<?php echo base_url('admin/Cash/add_saldo'); ?>", true);
        xhttp.setRequestHeader("Content-type", "application/x-www-form-urlencoded");
