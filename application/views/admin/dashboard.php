    function showSweetAlert(status, message) {
        Swal.fire({
            icon: status === 'success' ? 'success' : 'error',
            title: status === 'success' ? 'Berhasil' : 'Terjadi Kesalahan',
            text: message,
            timerProgressBar: true,
